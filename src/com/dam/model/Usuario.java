package com.dam.model;

public class Usuario {
	
	private String usuario;
	private String pwd;
	
	
	public Usuario(String usuario, String pwd) {
		this.usuario = usuario;
		this.pwd = pwd;
	}


	public String getUsuario() {
		return usuario;
	}


	public String getPwd() {
		return pwd;
	}
	
	
	
	

}
